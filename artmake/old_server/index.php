      <h2 id="sec01" class="pinkborderleft">施術について</h2>
      <dl class="faqlist">
        <dt>アートメイクは痛いですか？</dt>
        <dd>塗る麻酔をするので痛みはほとんどありません。</dd>
        <dt>アートメイクはどれくらい持ちますか？</dt>
        <dd>持続期間は個人差があり、1～3年です。キレイな仕上がりのため2回施術が必要となります。</dd>
        <dt>MRIは利用できますか</dt>
        <dd>問題ないと思われます。数千人に一人という極稀な割合でMRIを受けるときにピリピリ感じる事があります。クリニックで使用しているのはそのような事が起こらないといわれているメーカのピグメント（塗料）ですが、これからのMRI機器の性能向上の進化で反応する可能性もあります。MRIを受ける前には必ず医師にアートメイクをしている旨をお伝え下さい。</dd>
        <dt>何回施術が必要ですか？</dt>
        <dd>色の定着を確認し、きれいに仕上げる必要があるので2回施術で1セットです。</dd>
        <dt>アートメイクは誰でも受けられますか？？</dt>
        <dd>以降の方は、施術が出来ない場合がございます。妊娠中の方/心臓病、糖尿病（重度）/アトピー・ケロイド体質/感染症/ヘルペス/緑内障/皮膚病/</dd>
      </dl>
